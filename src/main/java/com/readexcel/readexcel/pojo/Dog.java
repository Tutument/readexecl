package com.readexcel.readexcel.pojo;

import org.springframework.stereotype.Component;


public class Dog {
    public String hand;
    public String id ;
    public String head;

    public String getHand() {
        return hand;
    }

    public void setHand(String hand) {
        this.hand = hand;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getHead() {
        return head;
    }

    public void setHead(String head) {
        this.head = head;
    }
}
